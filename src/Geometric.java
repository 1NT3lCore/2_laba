public class Geometric {
    public static void main(String[] args) {
        // Инициализация массива из 20 целых чисел
        int[] array = {5, -3, 10, -7, 0, -2, 8, -1, 4, -9, -4, 6, -5, 3, -8, 2, -6, 1, -10, 7};

        // Переменные для хранения произведения и количества отрицательных элементов
        double product = 1.0;
        int count = 0;

        // Проход по массиву
        for (int num : array) {
            if (num < 0) { // Проверка, является ли элемент отрицательным
                product *= num; // Умножение отрицательных элементов
                count++; // Увеличение счетчика отрицательных элементов
            }
        }

        // Вычисление среднего геометрического
        if (count > 0) {
            double geometricMean = Math.pow(product, 1.0 / count); // Формула среднего геометрического
            System.out.printf("Среднее геометрическое отрицательных элементов: %.4f%n", geometricMean);
        } else {
            System.out.println("Отрицательных элементов в массиве нет.");
        }
    }
}